    /// <summary>
    /// Lists every message kind understood by C64Net protocol version 1.
    /// </summary>
    public enum C64NetMessageType : ushort
    {
        /// <summary>
        /// First client-to-server handshake message containing protocol version,
        /// player name, password, and requested role.
        /// </summary>
        ClientHello = 1,
        /// <summary>
        /// Positive server-to-client handshake response with client id, stream
        /// dimensions, audio sample rate, accepted role, and joystick rights.
        /// </summary>
        ServerWelcome = 2,
        /// <summary>
        /// Negative server-to-client handshake response with a human-readable reason.
        /// </summary>
        ServerReject = 3,
        /// <summary>
        /// Client-to-server joystick state update encoded as active-low C64 bits.
        /// </summary>
        InputState = 4,
        /// <summary>
        /// Server-to-client completed C64 video frame.
        /// </summary>
        VideoFrame = 5,
        /// <summary>
        /// Server-to-client raw PCM audio bytes generated by the host SID.
        /// </summary>
        AudioChunk = 6,
        /// <summary>
        /// Server-to-client snapshot of every connected remote client.
        /// </summary>
        ClientList = 7,
        /// <summary>
        /// Server-to-client joystick permission update for the receiving client.
        /// </summary>
        PermissionUpdate = 8,
        /// <summary>
        /// Latency probe. The peer should answer with <see cref="Pong"/>.
        /// </summary>
        Ping = 9,
        /// <summary>
        /// Latency probe response that preserves the original ping timestamp.
        /// </summary>
        Pong = 10,
        /// <summary>
        /// Graceful leave/kick notification.
        /// </summary>
        Disconnect = 11,
        /// <summary>
        /// Server-to-client menu status text shown while the host is paused in a UI overlay.
        /// </summary>
        HostOverlayStatus = 12
    }

    /// <summary>
    /// Lists the client-visible session roles.
    /// </summary>
    public enum C64NetClientRole : byte
    {
        /// <summary>
        /// The client only watches the remote session unless the host later grants input rights.
        /// </summary>
        Observer = 0,
        /// <summary>
        /// The client requests player participation. The host still decides joystick rights.
        /// </summary>
        Player = 1
    }

    /// <summary>
    /// Lists joystick permissions granted by the host.
    /// </summary>
    public enum C64NetJoystickPermission : byte
    {
        /// <summary>
        /// No joystick input from this client reaches the emulated CIA.
        /// </summary>
        Observer = 0,
        /// <summary>
        /// Client input is wired to C64 joystick port 1.
        /// </summary>
        Port1 = 1,
        /// <summary>
        /// Client input is wired to C64 joystick port 2.
        /// </summary>
        Port2 = 2,
        /// <summary>
        /// Client input is mirrored into both C64 joystick ports.
        /// </summary>
        Both = 3
    }

    /// <summary>
    /// Represents a single framed network message.
    /// </summary>
    public sealed class C64NetMessage
    {
        /// <summary>
        /// Gets or sets the message type encoded into the fixed frame header.
        /// </summary>
        public C64NetMessageType Type { get; set; }
        /// <summary>
        /// Gets or sets reserved protocol flags. Version 1 currently leaves them unused.
        /// </summary>
        public ushort Flags { get; set; }
        /// <summary>
        /// Gets or sets an optional sender-side sequence number.
        /// </summary>
        public uint Sequence { get; set; }
        /// <summary>
        /// Gets or sets the sender timestamp in UTC ticks. Ping/pong uses this for latency.
        /// </summary>
        public long Timestamp { get; set; }
        /// <summary>
        /// Gets or sets the message body. An empty payload is represented by a zero-length array.
        /// </summary>
        public byte[] Payload { get; set; }
    }

    /// <summary>
    /// Represents a connected client snapshot for UI and protocol updates.
    /// </summary>
